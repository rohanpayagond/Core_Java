package com.xworkz.core_java.object_class_methods.to_string;

public class Fruit {

	String name="Banana";
	String color="Yellow";
	int weight=74;
	int price=125;
	
	@Override
	public String toString() {
		return "Fruit [name=" + name +", color=" + color +" ,weight=" +weight+", price=" +price+" ]";
	}
	
	
		
	
}
